package view;

/** 
 * Creates an instance of TetrisGUI and starts it. 
 * @author Erica Putsche
 * @version 05/31/2013
 */
public final class TetrisMain {
  
  /** Private constructor, to prevent instantiation of this class. */
  private TetrisMain() {
    throw new IllegalStateException();
  }
  
  /**
   * The main method, invokes the Tetris GUI. Command line arguments are
   * ignored.
   * 
   * @param the_args Command line arguments.
   */
  public static void main(final String[] the_args) {
    final TetrisGUI gui = new TetrisGUI();
    gui.start();
  }
}
